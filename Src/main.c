void timedStartup(void)
{
	printf("~~~~~ MCU Initialized ~~~~~\r\n");
	HAL_GPIO_WritePin(GPIOB,PWR_CTRL_Pin,GPIO_PIN_SET);

	HAL_TIM_Base_Start(&htim2);

	HAL_TIM_PWM_Start_IT(&htim2,TIM_CHANNEL_1);
	HAL_TIM_PWM_Start_IT(&htim2,TIM_CHANNEL_2);

	HAL_GPIO_WritePin(GPIOA,VREGEN_Pin,GPIO_PIN_RESET); //should boot to low anyway but be sure to force it here just in case
	initTickCount = HAL_GetTick();

	while(HAL_GetTick() < (initTickCount + 5)){} //keeps VREGEN low for +5ms then goes high
	HAL_GPIO_WritePin(GPIOA,VREGEN_Pin,GPIO_PIN_SET);

	while(HAL_GetTick() < (initTickCount + 10)){} //keeps reset high for +10ms then goes low
	HAL_GPIO_WritePin(GPIOA,BT_RST_Pin,GPIO_PIN_RESET);

	while(HAL_GetTick() < (initTickCount + 30)){} //keeps reset low for 20ms then goes high
	HAL_GPIO_WritePin(GPIOA,BT_RST_Pin,GPIO_PIN_SET);

	while(HAL_GetTick() < (initTickCount + 50)){}
	HAL_GPIO_WritePin(GPIOA,VREGEN_Pin,GPIO_PIN_RESET);

	HAL_TIM_PWM_Stop_IT(&htim2,TIM_CHANNEL_1);
	HAL_TIM_PWM_Stop_IT(&htim2,TIM_CHANNEL_2);

	HAL_UART_Transmit(&huart2,(uint8_t*)"RESET\r",6,1000);
}
